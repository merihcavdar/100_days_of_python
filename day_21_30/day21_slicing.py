piano_keys = ["a", "b", "c", "d", "e", "f", "g"]

print(piano_keys[2:5]) # between 2 and 5
print(piano_keys[2:]) # from 2
print(piano_keys[:5]) #up to 5
print(piano_keys[2:5:2]) # third parameter for increment
print(piano_keys[::2]) # every secnod item
print(piano_keys[::-1]) # reverse the list

